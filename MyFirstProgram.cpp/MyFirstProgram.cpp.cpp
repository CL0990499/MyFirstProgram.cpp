#include <iostream>
#include <string>  
using namespace std;

int main() {
    
    int myInt = 42;
    float myFloat = 3.14;
    double myDouble = 2.71828;
    char myChar = 'C';
    bool myBool = true;
    string myString = "Hello, World!";

    cout << "Integer: " << myInt << endl;
    cout << "Float: " << myFloat << endl;
    cout << "Double: " << myDouble << endl;
    cout << "Character: " << myChar << endl;
    cout << "Boolean: " << myBool << endl;
    cout << "String: " << myString << endl;

    return 0;
}
